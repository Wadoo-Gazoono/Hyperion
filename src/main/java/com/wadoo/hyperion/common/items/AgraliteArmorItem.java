public final class AgraliteArmorItem extends ArmorItem implements GeoItem {
    private final AnimatableInstanceCache cache = GeckoLibUtil.createInstanceCache(this);

    public AgraliteArmorItem(ArmorMaterial armorMaterial, EquipmentSlot type, Properties properties) {
        super(armorMaterial, type, properties);
    }

    // Create our armor model/renderer for forge and return it
    @Override
    public void initializeClient(Consumer<IClientItemExtensions> consumer) {
        consumer.accept(new IClientItemExtensions() {
            private GeoArmorRenderer<?> renderer;

            @Override
            public @NotNull HumanoidModel<?> getHumanoidArmorModel(LivingEntity livingEntity, ItemStack itemStack, EquipmentSlot equipmentSlot, HumanoidModel<?> original) {
                if (this.renderer == null)
                    this.renderer = new AgraliteArmorRenderer();

                this.renderer.prepForRender(livingEntity, itemStack, equipmentSlot, original);

                return this.renderer;
            }
        });
    }

    @Override
    public void registerControllers(AnimatableManager.ControllerRegistrar controllers) {
        controllers.add(new AnimationController<>(this, 20, state -> state.setAndContinue(DefaultAnimations.IDLE)));
    }

    @Override
    public AnimatableInstanceCache getAnimatableInstanceCache() {
        return this.cache;
    }

    @Override
    public void appendHoverText(ItemStack stack, @Nullable Level level, List<Component> tooltip, TooltipFlag flag) {
        super.appendHoverText(stack, level, tooltip, flag);
        tooltip.add(Component.translatable("item.hyperion.agralite_armor.desc").withStyle(ChatFormatting.GRAY));

        if(!Screen.hasShiftDown()) {
            tooltip.add(Component.translatable("item.hyperion.item_lore_description.desc").withStyle(ChatFormatting.ITALIC).withStyle(ChatFormatting.GRAY));
        }
        if(Screen.hasShiftDown()){
            tooltip.add(Component.translatable("item.hyperion.agralite_armor_lore.desc").withStyle(ChatFormatting.ITALIC).withStyle(ChatFormatting.AQUA));
        }

    }
}